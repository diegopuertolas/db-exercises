-- 3-NOMBRE Y APELLIDOS DE LOS EMPLEADOS CONTRATADOS EL 1 DE ENERO DE 2007
SELECT first_name, last_name FROM EMPLOYEES WHERE hire_date = '1/1/2007';

-- 4-NOMBRE DE LOS TRABAJOS CUYO SALARIO MINIMO ES MAYOR QUE 10.000
SELECT job_title FROM JOBS WHERE min_salary > 10000;

-- 5-DEL HISTORICO DE TRABAJOS, MUESTRA EL DEL EMPLEADO CUYO EMPLOYEE_ID=100;
