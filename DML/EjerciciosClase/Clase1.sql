-- Ejercicios consultas con filtros con operadores.

-- 1-MUESTRA EL CP DE LA CIUDAD DE ROMA
SELECT postal_code FROM LOCATIONS WHERE city = 'Roma';

-- 2-NOMBRE del DEPARTAMENTO con DEPARTMENT_ID=100
